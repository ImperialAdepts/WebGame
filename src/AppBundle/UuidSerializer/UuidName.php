	public static function getPlanetName($data)
	{
        $planetNames = array_merge(self::$periodicNames, self::$fabricNames);
		$planetUniqueUuid = implode('', $data);
		$nameCombinations = count(self::$colorNames) * count($planetNames);
		$combination = 0;
		foreach (str_split($planetUniqueUuid) as $letter) {
			$combination = ($combination + 7*ord($letter)) % $nameCombinations;
		}
		$nameIndex = $combination % count($planetNames);
		$colorIndex = $combination % count(self::$colorNames);
		return ucfirst(self::$colorNames[$colorIndex]) . ' ' .$planetNames[$nameIndex];
	}

    public static function getSettlementName(Settlement $data)
    {
        $adjectiveNames = self::$colorNames;
        $cityNames = self::$cityNames;
        $cityUuid = implode('-', [
            $data->getId(),
            $data->getAdministrativeCenter()->getYcoord(),
            $data->getAdministrativeCenter()->getXcoord(),
        ]);
        $nameCombinations = count($adjectiveNames) * count($cityNames);
        $combination = 0;
        foreach (str_split($cityUuid) as $letter) {
            $combination = ($combination + 7*ord($letter)) % $nameCombinations;
        }
        $nameIndex = $combination % count($cityNames);
        $adjectiveIndex = $combination % count($adjectiveNames);
        return ucfirst($adjectiveNames[$adjectiveIndex]) . ' ' .$cityNames[$nameIndex];
    }

